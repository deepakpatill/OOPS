using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OOPS
{
    class DataTypes
    {
        public void DataTypesDemo()
        {
            string a = "Test";
            int b = 1;
            float c = 1.0f;
            char d = 'A';
            bool e = true;
            Console.WriteLine("String data type: {0}", a);
            Console.WriteLine("Integer data type: {0}", b);
            Console.WriteLine("Floating/Decimal data type: {0}", c);
            Console.WriteLine("Character data type: {0}", d);
            Console.WriteLine("Boolean data type: {0}", e);

        }
    }
}
